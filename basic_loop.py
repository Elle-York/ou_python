# Draw a line of length 100 units using a loop
# Move 100 units but move 10 steps at a time
from turtle import*
for section in range(1, 11):      # Range starts at 1, increments by 1, and stops at 10
                forward(10)                  # Indent by 4 spaces


# We can also express the range differently
from turtle import*
for section in range(10):          # Range of whole numbers from 0 to 9
                forward(10)                 # Still does the same thing as the range above

                
                
